import { localeValue, type Locale } from "../lib/i18n";
import type { SubmissionDetail } from "../lib/schemas";

export type EditableRunCase = {
  id: string;
  input: string;
  expected_output: string;
};

export type RunSnapshot = {
  submissionId: string;
  cases: EditableRunCase[];
};

type DiffSegment = {
  text: string;
  different: boolean;
};

type DiffLine = {
  line: number;
  expected: DiffSegment[];
  actual: DiffSegment[];
  different: boolean;
};

